<h1>Foreword</h1>
[current-user:profile-library-registration:field-library-reg-system]
<p>
Thank you for participating in the IMPACT Survey pilot testing!  Your library’s involvement in this activity
is a key step forward in providing long-term assessment tools that collect data
on the impact your public access technology services are having in your communities.
We hope you will find this information useful for internal decision-making,
demonstrating the value of offering such services to stakeholders and elected
officials, and for advocacy efforts with the public.
</p>

<p>
The IMPACT Survey continues the work of the 2009 U.S. IMPACT Study, the first
large-scale investigation of the ways U.S. library patrons use computers and
the Internet at public libraries, why they use it, and how it affects their
lives. The study consisted of 4 case studies, a national telephone survey, and
an online survey designed to supplement the telephone survey and ensure that
PAC (public access computing) users from all walks of life were represented.
Over 400 libraries participated in the online survey, which yielded 45,000
responses. The study report <i>Opportunity
for All: How the American Public Benefits from Internet Access at U.S.
Libraries </i>can be accessed at <a href="http://impact.ischool.washington.edu/newsite/us-public-library-study.html">http://impact.ischool.washington.edu/newsite/us-public-library-study.html</a>.
</p>

<p>
The study findings have helped public libraries throughout the United States
advocate for funding and support of public access computing.  Because the patron web survey was so
successful, the University of Washington, in partnership with the International
City /County Management Association, and with generous support from the Bill
& Melinda Gates Foundation, has extended the benefits of the U.S. IMPACT
patron web survey by making it available to all public libraries to use in
their own data collection, evaluation, and advocacy efforts.
</p>

<p>
This report summarizes the ways your patrons use the public accessing computing
provided by [s:lib_name] based on the IMPACT Survey results. The survey was available to users from MONTH
DD, YYYY until MONTH DD, YYYY ; [s:Z_method_n] patrons completed the survey. The report is divided into four
sections:  
</p>

<ul>
  <li>
  <b>The library profile</b> provides an aggregated set of
  information about public access computing resources managed by the library. Research
  has found that these inputs have an effect on patron outcomes, and are
  important to consider when thinking about the survey results, 
  </li>
  
  <li>
  The
  <b>overall results</b> sectilon summarizes
  frequency and type of use of the public access computing resources and services
  provided by [s:lib_name].
  </li>
  
  <li>
  The section on <b>public access technology by
  activity area</b> reports on the seven domain specific uses by patrons:  civic engagement, eGovernment, eBusiness,
  education, health and wellness, employment, eCommerce, and social inclusion.
  </li>
  
  <li>
  Finally, the <b>patron satisfaction and demographics</b>
  section provides aggregate details on the types of patrons that completed the
  survey, and how satisfied they are with the resources provided by the library.
  </li>
</ul>

<p>
Overall, the information from the survey provides an overview of how the community
benefits from the library’s provision of free access to computers and the
Internet for patrons.  To help you make
effective use of these results go to the IMPACT Survey website to access a
number of additional advocacy tools (<a
href="http://impactsurvey.org/advocacy">http://impactsurvey.org/advocacy</a>).
</p>

